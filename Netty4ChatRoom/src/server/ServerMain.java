package server;

import io.netty.bootstrap.ServerBootstrap;
import io.netty.channel.ChannelFuture;
import io.netty.channel.ChannelOption;
import io.netty.channel.EventLoopGroup;
import io.netty.channel.nio.NioEventLoopGroup;
import io.netty.channel.socket.nio.NioServerSocketChannel;

public class ServerMain
{
	public static void main(String[] args)
	{
		ServerBootstrap serverBootstrap = new ServerBootstrap();
		EventLoopGroup boss = new NioEventLoopGroup();
		EventLoopGroup worker = new NioEventLoopGroup();
		try
		{
			serverBootstrap.group(boss, worker); // 设置监听线程，数据处理线程

			serverBootstrap.channel(NioServerSocketChannel.class);

			serverBootstrap.childHandler(new MyChannelInitializer());

			serverBootstrap.option(ChannelOption.SO_BACKLOG, 2048); // 底层IOListen的队列数
			serverBootstrap.option(ChannelOption.SO_KEEPALIVE, true); // 清除死连接
			serverBootstrap.option(ChannelOption.TCP_NODELAY, true); // 取消延迟发送
			ChannelFuture future = serverBootstrap.bind(5555);
			System.out.println("ChatRoom Server Start..." + System.currentTimeMillis());

			future.channel().closeFuture().sync();
		}
		catch (Exception e)
		{
			e.printStackTrace();
		}
		finally
		{
			boss.shutdownGracefully();
			worker.shutdownGracefully();
		}
	}
}
